# Racunske operacije

# Mnozenje
print(5 * 6)        # 5 i 6 se nazivaju operandima

# Sabiranje
print(5 + 6)

# Oduzimanje
print(5 - 6)

# Dijeljenje
print(10 / 2)       # Rezultat je 5.0 umjesto 5


# Cjelobrojno djeljenje
print(10 // 2)      # Rezultat je sada 5

# Stepenovanje
print(10 ** 3)

# Ostatak dijeljenja
print(10 % 3)

# Neki primjeri izraza
print(5 + 5 + 5)
print(5 + 5 * 5)


# Voditi racuna o prednosti operacija
# **, *, /, % imaju prednost u odnosu na +, -
print(5 - 2 * 2)

# Prvo se racuna izraz u zagradi
print((5 - 2) * 2)


# Stampanje broja minuta u danu
print(60 * 24)

# Stampanje broja sekundi u danu
print(60 * 60 * 24)
