# Varijable

# Predavanje varijable stringu
index = 2
print("{0} * {1} = {2}". format(5, index, 5 * index))

# Promjenom vrijednosti varijable stringu ce se predati nova vrijednost
index = 3
print("{0} * {1} = {2}". format(5, index, 5 * index))

# Kreiranje i predavanje vise varijabli stringu
a = 6
b = 7
c = 8
print("{0} + {1} + {2} = {3}". format(a, b, c, a + b + c))

# Stampanje varijabli
i = 1
print(i)
print(i * 2)

# Validni nacini nazivanja varijabli
# Naziv varijable ne moze zapoceti sa brojem
count = 5
_count = 4
Count = 2
count12345 = 5

# Dodjeljivanje vrijednosti jedne varijable drugoj varijabli
i = 5
j = i
j = 2 * i
print(j)

# Predavanje vrijednosti dvije varijable trecoj varijabli
num1 = 5
num2 = 3
sum = num1 + num2
print(sum)

# Predavanje i operacije sa varijablama u print naredbi
a = 5
b = 6
c = 7
print("{0} + {1} + {2} = {3}".format(a, b, c, a + b + c))

