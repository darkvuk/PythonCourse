# Stampanje

# Mogu da se koristi i " i '
print("Hello")
print('Hello')

print("5 * 6 = 30")
print(5 * 6)

# Neke funkcije koje rade samo sa brojevima
print(abs(10.5))        # Apsolutna vrijednost
print(pow(2, 5))        # Stepenovanje
print(max(34, 45, 67))  # Najveci broj
print(min(34, 45, 67))  # Najmanji broj

# Space se postuje
print("  Hello     w0rld!")

# "
print("Hello\"")
print('Hello"')

# Novi red
print("Hello\nWorld")

# Tab
print("Hello\tWorld")

# \
print("\\")
print("Hello\\\\\\World")